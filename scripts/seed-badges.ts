async function seedBadges() {
  console.log("üèÖ Seeding badge definitions...\n");

  try {
    // Insert badges
    for (const badge of BADGE_DEFINITIONS) {
      try {
        await db.insert(badgeDefinitions).values({
          name: badge.name,
          description: badge.description,
          icon: badge.icon,
          category: badge.category,
          tier: badge.tier,
          criteria: badge.criteria as any,
          criteriaDescription: badge.criteriaDescription,
          isAutomatic: badge.isAutomatic,
          displayOrder: badge.displayOrder,
        }).onConflictDoNothing();
        
        console.log(`  ‚úÖ ${badge.icon} ${badge.name} (${badge.category} - ${badge.tier})`);
      } catch (error) {
        console.log(`  ‚ö†Ô∏è ${badge.name} - already exists or error`);
      }
    }

    console.log(`\n‚ú® Seeded ${BADGE_DEFINITIONS.length} badge definitions!`);
  } catch (error) {
    console.error("Failed to seed badges:", error);
    process.exit(1);
  }

  process.exit(0);
}

